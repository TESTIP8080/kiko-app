function useIsMobile(){
  const[m,setM]=useState(()=>typeof window!=='undefined'&&window.innerWidth<768);
  useEffect(()=>{
    const h=()=>setM(window.innerWidth<768);
    window.addEventListener("resize",h);return()=>window.removeEventListener("resize",h);
  },[]);
  return m;
}

// ── useGeolocation ──
function useGeolocation(){
  const[loc,setLoc]=useState(DEFAULT_LOC);
  const[loading,setLoading]=useState(true);
  useEffect(()=>{
    if(!navigator.geolocation){setLoading(false);return;}
    navigator.geolocation.getCurrentPosition(
      pos=>{
        setLoc({lat:pos.coords.latitude,lng:pos.coords.longitude,name:"Ваше местоположение"});
        fetch(`https://nominatim.openstreetmap.org/reverse?lat=${pos.coords.latitude}&lon=${pos.coords.longitude}&format=json&accept-language=ru`)
          .then(r=>r.json())
          .then(d=>{
            const city=d.address?.city||d.address?.town||d.address?.village||"";
            const country=d.address?.country||"";
            if(city)setLoc(l=>({...l,name:`${city}, ${country}`}));
          }).catch(()=>{});
        setLoading(false);
      },
      ()=>setLoading(false),
      {timeout:8000,maximumAge:300000}
    );
  },[]);
  return{loc,loading};
}

// ── useCompass ──
function useCompass(){
  const[heading,setHeading]=useState(null);
  const[supported,setSupported]=useState(false);
  const[permissionNeeded,setPermissionNeeded]=useState(false);
  const[permissionDenied,setPermissionDenied]=useState(false);
  const cleanupRef=useRef(null);

  const startListening=useCallback(()=>{
    if(cleanupRef.current)cleanupRef.current();
    const handler=e=>{
      if(e.webkitCompassHeading!==undefined){setHeading(e.webkitCompassHeading);setSupported(true);}
      else if(e.alpha!==null){setHeading((360-e.alpha)%360);setSupported(true);}
    };
    window.addEventListener('deviceorientation',handler,true);
    const off=()=>window.removeEventListener('deviceorientation',handler,true);
    cleanupRef.current=off;
    return off;
  },[]);

  const requestPermission=useCallback(async()=>{
    if(typeof DeviceOrientationEvent!=='undefined'&&typeof DeviceOrientationEvent.requestPermission==='function'){
      try{
        const p=await DeviceOrientationEvent.requestPermission();
        if(p==='granted'){setPermissionNeeded(false);setPermissionDenied(false);startListening();}
        else{setPermissionDenied(true);}
      }catch{setPermissionDenied(true);}
    }
  },[startListening]);

  useEffect(()=>{
    if(typeof DeviceOrientationEvent==='undefined')return;
    if(typeof DeviceOrientationEvent.requestPermission==='function'){
      setPermissionNeeded(true);
    }else{
      startListening();
    }
    return()=>{if(cleanupRef.current){cleanupRef.current();cleanupRef.current=null;}};
  },[startListening]);

  return{heading,supported,permissionNeeded,permissionDenied,requestPermission};
}

